package ex02_ArrayList;

public class Person {
	
	private String name;
	private int age;
	private String tel;
	
	
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setAge(int age)
	{
		this.age = age;
	}
	
	public int getAge()
	{
		return age;
	}
	
	public void setTel(String tel)
	{
		this.tel = tel;
	}
	
	public String getTel()
	{
		return tel;
	}
}
